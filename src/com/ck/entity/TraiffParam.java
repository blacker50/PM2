package com.ck.entity;

public class TraiffParam {
	private Integer id;
	private String issue_month;
	private Double unit_price;
	private Double basic_power;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getIssue_month() {
		return issue_month;
	}
	public void setIssue_month(String issue_month) {
		this.issue_month = issue_month;
	}
	public Double getUnit_price() {
		return unit_price;
	}
	public void setUnit_price(Double unit_price) {
		this.unit_price = unit_price;
	}
	public Double getBasic_power() {
		return basic_power;
	}
	public void setBasic_power(Double basic_power) {
		this.basic_power = basic_power;
	}
	
}
